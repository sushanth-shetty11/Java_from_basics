class p18
{
	public static void main(String[] args)
	{
		 char c='B';
		
		switch(c)
		{
			case 'A' : System.out.println("Apple");
			break;
			case 'B' : System.out.println("Ball");
			break;
			case 'C' : System.out.println("Cat");
			break;
			case 'D' : System.out.println("Dog");
			break;
			
			
			default: System.out.println("Invalid choice");
		}
		
	}
}